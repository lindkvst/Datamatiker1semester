public class Adder
{
    public static long add(long a,long b){return a+b;}

    public static int add(int a, int b)
    {
        return a + b;
    }

    public static short add(short a, short b) {
       //
        return(short)(a+b);
    }

    public static byte add(byte a,
                           byte b) {
        return (byte)
                (a + b);
    }
}
